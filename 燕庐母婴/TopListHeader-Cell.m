#import "TopListHeader-Cell.h"

@implementation TopListHeader_Cell


- (id)initWithFrame:(CGRect)frame
{
    self = [super initWithFrame:frame];
    if (self) {
        
        self.headerImg = [[UIImageView alloc] init];
        [self.headerImg setTag:1];
        [self addSubview:self.headerImg];
        
        
        self.titleLabel = [[UILabel alloc] init];
        [self.titleLabel setTag:2];
        [self.headerImg addSubview:self.titleLabel];
    }
    return self;
}

@end
